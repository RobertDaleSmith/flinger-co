                <div class="blog_post_title">How it Works</div>
                <div class="blog_post_body">
                    Flinger.co is a web app that enables interaction between just about any connected TV that has a web browser and a laptop, tablet, or smart phone. Simply create a new channel on the big screen 
                    at Flinger.co. Then use the unique six digit code to pair to this channel from any second screen device. 
                    <br><br>
                    We have created a variety of Flinger.co Remote apps to enable you to control your
                    channel from whatever device you have in-hand while watching TV. Don't have a smart TV? ..no problem because you can use any PC/Mac with a browser to run the Flinger.co viewer from.
                    <br><br>
                    Not only can an unlimited number of remotes connect to a single channel, but also an unlimited number of viewers can connect to a single channel. Simply invite friends to watch with you using the
                    unique url matching your six digit Flinger channel code. Also chat with friends and see the conversation overlay the video on-screen using the Flinger.co Remote. 
