                <div class="blog_post_title">Our Story</div>
                <div class="blog_post_body">
                    We are a startup born in a dorm. It all started about a year ago when our founder, Robert received a Google TV device from a developer contest. 
                    This being his first smart TV he thought it would be a great way to watch web content on his TV. Although, he found himself 
                    still watching web content on his laptop because it was just so tedious to find and navigate to online video through the TV.
                    <br><br>
                    Based on this frustration, he built this really great browser extension called <a href="http://chromemote.com/" target="_blank">Chromemote</a>, 
                    which allows you to completely control any Google TV from any laptop running Google Chrome. Chromemote enables complete control of the TV and 
                    makes it super easy to send YouTube videos or anything with a URL to your TV.
                    <br><br>
                    Google liked the idea so much that a couple of their Google TV developers built a special <a href="http://code.google.com/p/google-tv-chrome-extensions/" target="_blank">plugin</a> 
                    for Chrome that enables Chromemote to work the way it does. After successfully pitching the idea at 
                    <a href="http://3daystartup.org/companies-founded-by-3ds-alumni/" target="_blank">3 Day Startup</a>, we launched Chromemote and it really took off within the Google TV community.
                    <br><br>
                    This past semester we were one of six teams selected to participate in the University of Houston's startup accelerator <a href="http://redlabs.bauer.uh.edu/" target="_blank">Red Labs</a>. 
                    Along with Red Labs, we also were accepted into the Microsoft Imagine Cup Student Accelerator. In March we were selected as one of the top fifteen teams in the US, and Microsoft 
                    flew us out to Silicon Valley for an intense week long <a href="https://web.archive.org/web/20130815004055/http://icsocialmediateam.com/2013/04/09/the-us-imagine-cup-startup-bootcamp-five-days-of-knowledge-experience-and-fun/" target="_blank">startup bootcamp</a>.
                    <br><br>
                    Over that week in Silicon Valley, Microsoft really pushed us to scale our idea to a much larger audience than only Google TVs. So we built Flinger.co, an entirely new platform that brings 
                    the best features of Chromemote to just about any TV and beyond just the Google TV platform.
                    <br><br><center><img src="../images_pages/team.png" alt="Flinger.co Team (Robert / Alex)"><br>Founders, Robert & Alex @ The Plug & Play Tech Center</center>
